# 2. 幻覺清洗函式 (Hallucination Killer)
# ==========================================
def clean_hallucinations(text):
    """
    暴力清洗語音模型常見的 YouTube 字幕幻覺
    """
    if not text: return ""

    # 1. 黑名單關鍵字 (直接刪除包含這些詞的整句或片段)
    blacklist_patterns = [
        r"請訂閱.*", r"訂閱頻道.*", r"點讚.*分享.*", r"開啟小鈴鐺.*",
        r"歡迎訂閱.*", r"Thanks for watching.*", r"Copyright.*", 
        r"不吝指教.*", r"打卡分享.*", r"字幕製作.*", r"AI字幕.*",
        r"由.*製作", r"Provided to YouTube", r"Auto-generated by"
    ]
    
    cleaned_text = text
    for pattern in blacklist_patterns:
        # 使用正則表達式刪除 (IgnoreCase)
        cleaned_text = re.sub(pattern, "", cleaned_text, flags=re.IGNORECASE)

    # 2. 清洗多餘的重複符號
    cleaned_text = re.sub(r'\s+', ' ', cleaned_text).strip()
    
    # 3. 如果清洗後字數過少 (例如只剩 "謝謝"), 視為無效
    if len(cleaned_text) < 2:
        return ""
        
    return cleaned_text

